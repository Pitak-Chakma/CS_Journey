public class UnaryOperators {
    public static void main(String[] args) {
        int num = 10;
        //PostIncrement - use first then add (most common)
        System.out.println(num++);
        System.out.println(num);

        //PreIncrement - add first then add
        System.out.println(++num);
        System.out.println(num);

        //same for subtraction
    }

}
