public class ArithmeticOperator {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 2;

        System.out.println("Int Operations:");
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);

        float num3 =5;
        float num4 =2;
        System.out.println("Float Operations:");
        System.out.println(num3 + num4);
        System.out.println(num3 - num4);
        System.out.println(num3 * num4);
        System.out.println(num3 / num4);
        System.out.println(num3 % num4);


    }
}
